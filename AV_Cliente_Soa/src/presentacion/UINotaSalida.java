    @Override
    public void datoSelect(int origen, Object dato) {
            switch (origen){
         case PERSONA_TIPE :{
                                Persona d = (Persona)dato;
                                this.idPer = String.valueOf(d.getId());
                                jCTextPersonal.setText(d.toString());    
         
         }
            break;
         case ALMACEN_TIPE :{
                           Almacen d = (Almacen)dato;
                           this.idAlm = String.valueOf(d.getId());
                           jCTextAlmacen.setText(d.toString());
         }  
                        break;
          case ROLLO_TIPE :{
                        List<Rollo> r = (List<Rollo>)dato;
                        actualizarTabla(r);
         }  
                        break;
           default:
                   break;
        }
    }

