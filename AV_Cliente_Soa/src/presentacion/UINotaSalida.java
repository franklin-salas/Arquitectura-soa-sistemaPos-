    private void actualizarTabla(List<Rollo> rollos) {
            listasDetalle = new LinkedList<>();
        for (Rollo rollo : rollos) {
            Detalle d = servicioNota.getDetalle(String.valueOf(rollo.getId()));
            listasDetalle.add(d);
            this.montoTotal = montoTotal + d.getPrecio();
        }
        listar(listasDetalle);
        jCTextTotal.setText(String.valueOf(montoTotal));
    }

    private void listar(List<Detalle> datos) {
                     DefaultTableModel tipoDetalles = new DefaultTableModel();
              tipoDetalles.setColumnIdentifiers(new String[]{"Nro", "Metraje", "Precio", "Descricion"});
              for (int i = 0; i < datos.size(); i++) {
                  tipoDetalles.addRow(new String[]{
                      String.valueOf(i+1),
                 String.valueOf( datos.get(i).getMetraje()),
                 String.valueOf(datos.get(i).getPrecio()),
                 datos.get(i).getDescripcion()             
                });
              }
        this.listasDetalle = datos;
        jTable1.setModel(tipoDetalles); 
    }
  

