    public void setDato(Almacen almacenSelet) {
        this.id = String.valueOf(almacenSelet.getId());
        jCTextCodigo.setText(almacenSelet.getCodigo());
        jCTextDescripcion.setText(almacenSelet.getDescripcion());
        jCTextDireccion.setText(almacenSelet.getDireccion());
    }
    

    private void actualizartabla(){
      this.listar(servicioAlmacen.listarAlmacen());
    }
        private void listar(List<Almacen> datos){
        
        DefaultTableModel almacenes = new DefaultTableModel();
        almacenes.setColumnIdentifiers(new String[]{"Codigo", "Descripci贸n", "Direcci贸n"});
        for (int i = 0; i < datos.size(); i++) {
             almacenes.addRow(new String[]{
                 datos.get(i).getCodigo(),
                 datos.get(i).getDescripcion(),
                 datos.get(i).getDireccion()
                });
        }
        this.datos = datos;
        jTable1.setModel(almacenes);
    }
        
    private void limpiarCampos(){
        this.id =null;
        jCTextCodigo.setText("");
        jCTextDescripcion.setText("");
        jCTextDireccion.setText("");
        
    }
