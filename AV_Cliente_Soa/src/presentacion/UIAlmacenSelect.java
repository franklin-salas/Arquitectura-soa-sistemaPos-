    private void listar(List<Almacen> datos){
        DefaultTableModel almacenes = new DefaultTableModel();
        almacenes.setColumnIdentifiers(new String[]{"Codigo", "Descripci贸n", "Direcci贸n"});
        for (int i = 0; i < datos.size(); i++) {
             almacenes.addRow(new String[]{
                 datos.get(i).getCodigo(),
                 datos.get(i).getDescripcion(),
                 datos.get(i).getDireccion()
                });
        }
        jTable1.setModel(almacenes);
    }

